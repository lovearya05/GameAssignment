export const textwhite = (fontSize = 12, fontWeight = 400) => ({
    fontSize: fontSize,
    fontWeight: fontWeight,
    color: '#fff'
})
export const textOffWhite = (fontSize = 12, fontWeight = 400) => ({
    fontSize: fontSize,
    fontWeight: fontWeight,
    color: 'rgba(217, 217, 217, 1)'
})
export const textBlk = (fontSize = 12, fontWeight = 400) => {
    return {
        fontSize: fontSize,
        fontWeight: fontWeight,
        color: '#000'
    }
}
